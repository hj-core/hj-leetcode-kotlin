package com.hj.leetcode.kotlin.problem1688

/**
 * LeetCode page: [1688. Count of Matches in Tournament](https://leetcode.com/problems/count-of-matches-in-tournament/);
 */
class Solution {
    /* Complexity:
     * Time O(1) and Space O(1);
     */
    fun numberOfMatches(n: Int): Int {
        return n - 1
    }
}